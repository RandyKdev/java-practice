class DataTypes {
    public static void main(String[] args) {
        // primitive types
        int i = 2;
        double j = 4.3;
        char c = 'g';
        String str = "Hey";
        boolean bool = true;

        System.out.println(i);
        System.out.println(j);
        System.out.println(c);
        System.out.println(str);
        System.out.println(bool);
    }
}