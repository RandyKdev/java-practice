import java.util.ArrayList;

public class arrayList {
    public static void main(String[] args) {
        // ArrayList is used to create a dynamic arrays of elements
        ArrayList<Integer> s = new ArrayList<Integer>();
        s.add(1); // adds an element in to array list
        s.size(); // gets the size of the list
        s.get(4); // gets element at index 4
        s.remove(2); // removes element at index 2
    }
}
